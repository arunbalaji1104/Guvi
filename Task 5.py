"""3) Write a program that takes a string and returns a new string with all the vowels
removed.
"""

def remove_vowels(input_string):
    vowels = "aeiouAEIOU"
    new_string = ""
    for char in input_string:
        if char not in vowels:
            new_string += char
    return new_string


