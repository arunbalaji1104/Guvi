"""6. Write a program that takes two strings and returns the longest common substring
between them.

"""

def longest_common_substring(str1, str2):
    m = len(str1)
    n = len(str2)


    dp = [[0] * (n + 1) for _ in range(m + 1)]


    max_len = 0

    end_index = 0


    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if str1[i - 1] == str2[j - 1]:
                dp[i][j] = dp[i - 1][j - 1] + 1
                if dp[i][j] > max_len:
                    max_len = dp[i][j]
                    end_index = i - 1


    longest_substring = str1[end_index - max_len + 1: end_index + 1]
    return longest_substring


string1 = input("Enter the first string: ")
string2 = input("Enter the second string: ")

result = longest_common_substring(string1, string2)
print("Longest common substring:", result)

"""7) Write a program that takes a string and returns the most frequent character in it.

"""

def most_frequent_character(input_string):
    char_count = {}
    for char in input_string:
        if char in char_count:
            char_count[char] += 1
        else:
            char_count[char] = 1


    max_count = 0
    most_frequent_char = None
    for char, count in char_count.items():
        if count > max_count:
            max_count = count
            most_frequent_char = char

    return most_frequent_char


input_string = input("Enter a string: ")

result = most_frequent_character(input_string)
if result:
    print("Most frequent character:", result)
else:
    print("The string is empty.")

